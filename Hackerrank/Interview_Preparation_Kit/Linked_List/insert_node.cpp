SinglyLinkedListNode* insertNodeAtPosition(SinglyLinkedListNode* head, int data, int position) 
{
    
    SinglyLinkedListNode* new_node = new SinglyLinkedListNode(data); 

    if(position == 0) // insert the node at the head
    {
        new_node -> next = head;
        head = new_node;//adjust head
        return head;
    }

    // traverse the node before the node to be inserted and insertion
    int pos_cnt = 0;
    SinglyLinkedListNode* cur_node = head;
    while(cur_node != NULL)
    {
        if(pos_cnt == position - 1) // before that node
        {
            break;
        }
        cur_node = cur_node -> next;
        pos_cnt++;
    }
    
    // insertion process and rearrange
    
    new_node -> next = cur_node -> next -> next;
    cur_node -> next = new_node;
    // traverse(head);
    return head;
}
